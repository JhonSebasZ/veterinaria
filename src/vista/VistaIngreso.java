    public void llenarCombobox(){
        cbxRaza.addItem("Seleccione");
        
        razas = rSql.getRazas();
        
        //llena el combobox
        for (int i = 0; i < razas.size(); i++) {
            this.cbxRaza.addItem(razas.get(i).getNombreRaza());
        }
    }

    public void definirColumTabla() {
        modelo.addColumn("CODIGO");
        modelo.addColumn("RESPONSABLE");
        modelo.addColumn("MASCOTA");
        modelo.addColumn("RAZA");
        modelo.addColumn("NUMERO");
    }

    public void mostrarDatos() {
        
        mascotas = mSql.getMascotas();
        modelo.setNumRows(this.mascotas.size());
        
        //llena la tabla con los datos de las mascotas
        for (int i = 0; i < mascotas.size(); i++) {
            modelo.setValueAt(mascotas.get(i).getCodigo(), i, 0);
            modelo.setValueAt(mascotas.get(i).getNombreResponsable(), i, 1);
            modelo.setValueAt(mascotas.get(i).getNombreMascota(), i, 2);
            modelo.setValueAt(mascotas.get(i).getRaza().getNombreRaza(), i, 3);
            modelo.setValueAt(mascotas.get(i).getNumeroContacto(), i, 4);
        }
        tblMascotas.setModel(modelo);
    }

