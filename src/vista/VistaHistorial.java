    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Historial h = new Historial();
        Procedimiento p = new Procedimiento(procedimientos.get(cbxProcedimientos.getSelectedIndex() - 1).getCodigo(), procedimientos.get(cbxProcedimientos.getSelectedIndex() - 1).getNombre());

        h.setDescripcion(txtDescripcion.getText());
        h.setProcedimiento(p);
        h.setCodigoMascota(m.getCodigo());

        hSql.addHistial(h);

        this.mostrarDatos();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tlbHistorial.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un procedimiento");
        } else {
            Historial h = new Historial();
            h.setCodigo(historiales.get(tlbHistorial.getSelectedRow()).getCodigo());

            hSql.eliminarHistoria(h);
            this.mostrarDatos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

