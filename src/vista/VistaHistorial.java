    public void definirColumTabla() {
        modelo.addColumn("CODIGO");
        modelo.addColumn("PROCEDIMIENTO");
        modelo.addColumn("DESCRIPCION");
    }

    public void llenarCombobox() {
        procedimientos = pSql.getProcedimientos();

        this.cbxProcedimientos.addItem("Seleccione");
        for (int i = 0; i < this.procedimientos.size(); i++) {
            this.cbxProcedimientos.addItem(this.procedimientos.get(i).getNombre());
        }
    }

    public void mostrarDatos() {

        historiales = hSql.getHistorials(m);

        modelo.setNumRows(historiales.size());
        for (int i = 0; i < historiales.size(); i++) {
            modelo.setValueAt(historiales.get(i).getCodigo(), i, 0);
            modelo.setValueAt(historiales.get(i).getProcedimiento().getNombre(), i, 1);
            modelo.setValueAt(historiales.get(i).getDescripcion(), i, 2);
        }
        tlbHistorial.setModel(modelo);
    }

